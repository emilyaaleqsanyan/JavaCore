package homework.chapter2;

public class ForExample {
    public static void main(String[] args) {
        int x, y;
        y = 1;
        for (x = 30; x > 1; x--) {
            System.out.println("значение x:" + x);
            System.out.println("значение y:" + y);
            y++;
        }
    }
}
